//division
console.log("- 0 / 33 -> " + 0 / 33); //zero
console.log("- 33 / 0-> " + 33 / 0); //infinity type
console.log("- 33,1 / 3 -> " + "33,1" / 3); // not a number because of 33,1
console.log("- 33.1 / 3 -> " + "33.1" / 3); //convert 33.1 string to number 33.1

//addition
console.log("- 0.1 + 0.7 -> " + 0.1 + 0.7); // the addition with float may give inaccurate results because of IEEE standard
console.log("- '0.1' + 0.7 -> " + "0.1" + 0.7); //concact
