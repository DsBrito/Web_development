//a pair of {} represents an object and collection key : value
const obj = { name: "mouse", value: "49.90", type: "bluethooth" };
console.log("-  print obj -> ");
console.log(obj);

const objx = {};
objx.name = "mouse";
objx.value = "49.90;";
objx.type = "bluethooth";
console.log("-  print objx -> ");
console.log(objx);
